// --- int vs float output fidelity in tsLiteralFor ---

const IDENT_NUM_SRC = `
(define id-num
  {number --> number}
  X -> X)
`;

test("tsLiteralFor: int prints as integer, float with .0 suffix", () => {
  const sf = parseFile(IDENT_NUM_SRC);
  const tt = buildTypeTable(sf.datatypes, "", "");
  const h = buildHarness({
    spec: sf,
    tt,
    allDefines: sf.defines,
    funcName: "id-num",
    implModule: "./id_num",
    implFunc: "IdNum",
    importPath: "./shenguard_gen.ts",
    importAlias: "shenguard",
    maxCases: 10,
    seed: 0,
    randomDraws: 0,
  });
  // All 6 boundary samples.
  assert.equal(h.cases.length, 6);
  const expectedLiterals = h.cases.map((c) => c.expectedTs);
  // NUMBER_RAW order: 0,1,-1,5,(float 2.5),100.
  assert.deepEqual(expectedLiterals, ["0", "1", "-1", "5", "2.5", "100"]);
  // The float should remain 2.5. If evaluator had accidentally produced an
  // integer-valued float, we'd see "2" — guard against that.
  assert.ok(expectedLiterals.includes("2.5"));
});

