// --- Payment spec: 1-define harness with wrapper + constrained + composite ---

const PAYMENT_SRC = `
(datatype account-id
  X : string;
  ==============
  X : account-id;)

(datatype amount
  X : number;
  (>= X 0) : verified;
  ====================
  X : amount;)

(datatype transaction
  Amount : amount;
  From : account-id;
  To : account-id;
  ===================================
  [Amount From To] : transaction;)

(define processable
  {amount --> (list transaction) --> boolean}
  B0 Txs -> (foldr (lambda X (lambda Acc (and (>= (val X) 0) Acc)))
              true
              (scanl (lambda B (lambda Tx (- (val B) (val (amount Tx))))) (val B0) Txs)))
`;

test("TestBuildHarnessPayment: builds cases, cases are booleans, emit contains key substrings", () => {
  const sf = parseFile(PAYMENT_SRC);
  assert.equal(sf.defines.length, 1);
  const tt = buildTypeTable(
    sf.datatypes,
    "./shenguard_gen.ts",
    "shenguard",
  );

  const cfg: HarnessConfig = {
    spec: sf,
    tt,
    allDefines: sf.defines,
    funcName: "processable",
    implModule: "./processable",
    implFunc: "Processable",
    importPath: "./shenguard_gen.ts",
    importAlias: "shenguard",
    maxCases: 35,
    seed: 0,
    randomDraws: 0,
  };

  const h = buildHarness(cfg);
  assert.ok(h.cases.length > 0, "expected cases");
  assert.ok(h.cases.length <= 35);

  for (const c of h.cases) {
    assert.equal(c.expected.kind, "bool", `case ${c.label} expected bool`);
  }

  const src = emit(h);
  for (
    const want of [
      `import { test } from "node:test";`,
      `import assert from "node:assert/strict";`,
      `import * as shenguard from "./shenguard_gen.ts";`,
      `import { Processable } from "./processable";`,
      `Processable(`,
      `mustAmount`,
      `mustTransaction`,
      `mustAccountId`,
      `processable: spec says`,
    ]
  ) {
    assert.ok(
      src.includes(want),
      `emitted source missing ${JSON.stringify(want)}\n--- source ---\n${src}`,
    );
  }
});

